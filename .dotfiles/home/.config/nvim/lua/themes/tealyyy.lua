---@param config TealyyyConfig?
Tealyyy.setup = function(config)
  Tealyyy.config = vim.tbl_deep_extend("force", Tealyyy.config, config or {})
end

--- main load function
Tealyyy.load = function()
  if vim.version().minor < 8 then
    vim.notify_once("tealyyy.nvim: you must use neovim 0.8 or higher")
    return
  end
  -- reset colors
  if vim.g.colors_name then
    vim.cmd.hi("clear")
  end
  vim.g.colors_name = "tealyyy"
  vim.o.termguicolors = true
  local groups = get_groups()
  -- add highlights
  for group, settings in pairs(groups) do
    vim.api.nvim_set_hl(0, group, settings)
  end
end

return Tealyyy

